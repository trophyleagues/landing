            <h1>Política de cookies</h1>
            <p><strong>¿Qué son las cookies?</strong></p>
            <p>Una cookie es un archivo que se descarga en su ordenador al acceder a determinadas páginas web. Las
                cookies permiten a una página web, entre otras cosas, almacenar y recuperar información sobre los
                hábitos de navegación de un usuario o de su equipo y, dependiendo de la información que contengan y
                de
                la forma en que utilice su equipo, se pueden utilizar para reconocer al usuario.</p>
            <p>Las cookies son esenciales para el funcionamiento de internet aportando innombrables ventajas en la
                prestación de servicios, facilitando la navegación y la usabilidad de nuestra Web. Por eso, por el
                hecho
                de navegar por nuestra página web, entendemos que consiente expresamente la aceptación de nuestras
                cookies que detallamos en el presente documento.</p>
            <p>Hay que resaltar que puede gestionar las cookies en su navegador, le facilitamos la información en
                este
                aviso legal que le puede ser de utilidad. El hecho de restringir el uso de las cookies podría
                afectar a
                la navegación por nuestra web y puede comportar que no pueda disfrutar completamente de nuestros
                servicios y propuestas detalladas en la misma.</p>
            <p>Tenga en cuenta que las cookies no pueden dañar su ordenador y que a cambio el hecho de tenerlas
                activadas nos ayudan a identificar y resolver los errores.La información que le proporcionamos a
                continuación, le ayudara a comprender las diferentes tipos de cookies:</p>
            <p><strong>En relación al tiempo de vigencia:</strong></p>
            <p><strong>Cookies de sesión</strong>: Son cookies temporales que permanecen al archivo de cookies de su
                navegador hasta que abandone la sesión de la pagina web, de manera que nunca queda registrada en el
                disco duro del ordenador del usuario. La información obtenida mediante estas Cookies, sirve para
                analizar pautas de tránsito en la web. A la larga, esto nos permite proporcionar una mejor
                experiencia
                para mejorar los contenidos y facilitar su uso.</p>
            <p><strong>Cookies permanentes</strong>: son almacenables en el disco duro y nuestra web las lee cada
                vez
                que el usuario realiza una nueva visita. Una cookie permanente tiene una fecha de expiración
                determinada. Las cookies dejaran de funcionar después de esta fecha. Las utilizamos generalmente
                para
                facilitar los servicios de registro y análisis.</p>
            <p><strong>En relación a la propiedad de la emisión:</strong></p>
            <p><strong>Cookies propias</strong>: Son las emitidas por el titular de la web con un control sobre
                ellas.
            </p>
            <p><strong>Cookies de terceros</strong>: Emitidas por terceros en la que el propietario de la web no
                tiene
                mayor control que los servicios que contrata a ese tercero (ej. google analytics _ga) Por ello
                remitimos
                a la información por ellos establecida en su web en relación a estas cookies.</p>
            <p><strong>En relación a la finalidad que persiguen:</strong></p>
            <p><strong>Cookies Técnicas</strong>: Estas cookies son de carácter técnico y únicamente válido durante
                una
                sesión. Son necesarias para habilitar cierta funcionalidad de nuestra página.</p>
            <p><strong>Cookies de Análisis</strong>: Son aquéllas que bien tratadas por nosotros o por terceros, nos
                permiten realizar análisis estadístico de la utilización que hacen los usuarios del servicio
                ofertado.
                Para ello se analiza su navegación en nuestra página web con el fin de mejorar la oferta de
                productos o
                servicios que le ofrecemos.</p>
            <p><strong>Cookies Funcionales</strong>: Estas cookies nos permiten recordar preferencias y
                características
                generales, como por ejemplo el idioma.</p>
            <p><strong>Cookies publicidad</strong>: Son aquéllas que bien tratadas por nosotros o por terceros, nos
                permiten adecuar el contenido de la publicidad para que esta sea relevante para el usuario y para
                evitar
                mostrar anuncios que el usuario ya haya visto.</p>
